        public JsonResult Put(Employee emp)
        {
            string query = @"
                update dbo.Employee set 
                Name = '" + emp.Name + @"'
                ,Department = '" + emp.Department + @"'
                ,DateOfJoining = '" + emp.DateOfJoining + @"'
